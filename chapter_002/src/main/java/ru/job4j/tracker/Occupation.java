package ru.job4j.tracker;

public class Occupation {
    String name;
    String surname;
    String education;
    String birthday;

    public Occupation() {

    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public String getEducation() {
        return education;
    }

    public String getBirthday() {
        return birthday;
    }
}
